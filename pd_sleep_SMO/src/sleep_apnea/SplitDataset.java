    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String datasetAfterDR=FeatureExtraction.datasetAfterDR;
        String sp[]=datasetAfterDR.trim().split("\n");
        String sc[]=sp[0].trim().split(",");
        String newmetadata="@RELATION "+datasetNameWithoutExtension+"\n";
        for(int i=0;i<sc.length-1;i++)
        {
            newmetadata=newmetadata+"\n@ATTRIBUTE "+sc[i].trim().replaceAll(" ","_")+" REAL";
        }
        newmetadata=newmetadata+"\n@ATTRIBUTE "+sc[sc.length-1].trim().replaceAll(" ","_")+" {";
        String ro="";
        ArrayList norep=new ArrayList();
        for(int i=1;i<sp.length;i++)
        {
            ro=ro+sp[i].trim()+"\n";
            String sc1[]=sp[i].trim().split(",");
            String clslbl=sc1[sc1.length-1];
            if(!(norep.contains(clslbl.trim())))
            {
                norep.add(clslbl.trim());                        
            }
        }
        Collections.sort(norep);
        for(int i=0;i<norep.size();i++)
        {
            String clslbl=norep.get(i).toString().trim();
            newmetadata=newmetadata+clslbl.trim()+",";
        }
        newmetadata=newmetadata.substring(0,newmetadata.lastIndexOf(','));
        newmetadata=newmetadata+"}\n\n@DATA";
        
        String train="";
        for(int i=1;i<((sp.length*80)/100)-1;i++)
        {
            train=train+sp[i].trim()+"\n";
        }
        String trainingData=train.substring(0,train.lastIndexOf('\n'));
        String trainingDataset=newmetadata.trim().replaceAll(datasetNameWithoutExtension,(datasetNameWithoutExtension+"Training"))+"\n"+trainingData.trim();
        filewrite("TrainingDataset.arff",trainingDataset);
        jTextArea1.append("=================================================\n");
        jTextArea1.append("                  "+"Training Dataset"+"\n");
        jTextArea1.append("=================================================\n");
        jTextArea1.append(trainingDataset.trim());
        
        String test="";
        for(int i=((sp.length*80)/100)-1;i<sp.length;i++)
        {
            String te=sp[i].trim();
            test=test+te.substring(0,te.lastIndexOf(','))+",?\n";
        }
        String testingData=test.substring(0,test.lastIndexOf('\n'));
        String testingDataset=newmetadata.trim().replaceAll(datasetNameWithoutExtension,(datasetNameWithoutExtension+"Testing"))+"\n"+testingData.trim();
        filewrite("TestingDataset.arff",testingDataset);
        jTextArea2.append("=================================================\n");
        jTextArea2.append("                  "+"Testing Dataset"+"\n");
        jTextArea2.append("=================================================\n");
        jTextArea2.append(testingDataset.trim());
    }//GEN-LAST:event_jButton1ActionPerformed

